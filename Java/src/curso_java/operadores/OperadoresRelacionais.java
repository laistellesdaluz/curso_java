package curso_java.operadores;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		System.out.println(2 >  1); //dois é maior que 1?
		System.out.println(1 <  1); //1 é menor a 1?
		System.out.println(1 <= 1); //1 é maior ou igual a 1?
		System.out.println(1 >= 1); //1 é maior ou igual a 1?
		System.out.println(2 == 2); //2 é igual a 2?
		System.out.println(1 != 2); //1 é diferente de 2?
	}
}
